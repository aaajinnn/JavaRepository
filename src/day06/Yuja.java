package day06;

public class Yuja {
	private int yuja;
	private int sugar;

	// setter
	public void setYuja(int yuja) {
		this.yuja = yuja;
	}

	public void setSugar(int sugar) {
		this.sugar = sugar;
	}

	// getter
	public int getYuja() {
		return this.yuja;
	}

	public int getSugar() {
		return this.sugar;
	}
}
